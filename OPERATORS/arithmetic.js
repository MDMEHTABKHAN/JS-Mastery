let a = 10;
let b = 3;

console.log(a + b); 
console.log(a - b); 
console.log(a * b); 
console.log(a / b); // 3.333... (Division)
console.log(a % b); 
console.log(a ** b); // 1000 (Exponentiation)

a++; // Increment: a becomes 11
console.log(a); // 11

b--; // Decrement: b becomes 2
console.log(b); // 2