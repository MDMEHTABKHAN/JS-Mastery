let arr = [10,20,30,40,50,60,70];

// push() add one more elements at end of the end
arr.push(80)
console.log(arr);

// pop(): Removes the last element from an array
arr.pop()
console.log(arr);

// shift(): first elements of the array
arr.shift()
console.log(arr);

// unshift(): add first elements of the array
arr.unshift(5)
console.log(arr);

// contac:  add one or more then array

let newarr = arr.concat([90,100,110]);
console.log(newarr);





